          			<h3><strong>Our Patron – St. Thomas Aquinas</strong></h3>
          <p>St. Thomas Aquinas (1225 – 1274) was one of the great teachers of the medieval Catholic Church, honoured with the titles: Doctor of the Church, Angelic Doctor and the Prince of Catholic Theologians.</p>
          <p>He was born in Aquino, close to the Benedictine Monastery of Monte Casino, in central Italy where he had his early education. Later he was sent for studies to Naples, where he was exposed to the writings of Greek philosopher Aristotle. After his priesthood, he was sent for further studies to Cologne, and was under the tutelage of St. Albert the Great (1200 – 1280). St. Albert was so impressed by Thomas Aquinas, he urged that Thomas be sent to one of the famous universities in Paris.</p>

          <div class="container-fluid"  style="background-image: url(img/col.jpg); background-repeat: no-repeat;">
          <p>After obtaining the Ordination and Doctorate in Sacred Theology, he began teaching with such lucidity, accuracy and inspiration thus, attracting students in numbers; the University of Paris at that time accounted 30,000 students. Living at the court of Pope Urban IV in Paris with two Professorships to his name, Thomas Aquinas distinguished himself as the principal exponent of Scholastic Philosophy which interpreted Aristotle’s system in the light of Christian Teaching. In his relatively short life span of 49 years, he is known to have composed some sixty works. Far surpassing, everything else, was his unrivalled <strong><em>Summa Theological</em></strong>, a summary of Christian Philosophy and Theology. The beautiful Office and Mass for Corpus Christi, with its much loved hymns, <em>Tantum Ergo</em> and <em>O Salutaris Hostia</em>, were composed by him on the Pope’s orders.</p>
          <p>&nbsp;</p>
          <p>Thomas Aquinas died while on his way to the Council of Lyons. He was canonized by Pope John XXII on 18<sup>th</sup> July, 1323, elevated to the dignity of ‘Doctor of the Church’ by Pope Pius V on 11<sup>th</sup> April 1567, and in 1918, his name was included in the code of Canon Law. Among other citations, St. Thomas Aquinas has also been declared the Special <strong>Patron of all Catholic Universities and Educational Institutions.&nbsp; </strong></p>
          <p>We are fortunate to have such an intellectual giant and a holy priest as our Patron Saint. Let this humble and hard-working intellectual be the light and guide to all our lecturers and students.</p>




              <p style="text-align: justify;">The Aquinas College of Higher Studies was founded in 1953, by the late Very Rev. Fr. Peter A. Pillai OMI, Rector of St. Joseph’s College, at its premises in Darley Road, Colombo 10 as an autonomous higher educational institute with the approval, blessings and the inspiration of His Eminence Thomas Cardinal Cooray OMI., the 6<sup>th</sup> Archbishop of Colombo. Aquinas College was registered in 1954 by the then Ministry of Education Ceylon (as it was then called), and in 1954 moved in to their permanent abode at No.30, Maradana Road, Colombo 8.</p>
              <p style="text-align: justify;">The Aquinas College of Higher Studies is a nonprofit educational institution administered by the Catholic Church under the auspices of the Archdiocese of Colombo, devoted to education and philanthropic activities. It had contributed much to the development of the nation, by conducting numerous higher educational and professional programmes for the benefit of our youth in Sri Lanka.</p>
              <p style="text-align: justify;">Aquinas College of Higher Studies is situated in Parkland, Borella, located at the centre of the City of Colombo. The campus provides a very healthy and peaceful environment conducive to educational activities. The presence of students from many diverse backgrounds and nationalities provides a very cosmopolitan community, and as we also have students from SAARC countries&nbsp;&nbsp; belonging to various ethnic groups, their numerous cultures could be learnt with ease. Thus, it is now a national and an international community of scholars, enlivened by Christian inspiration, engaged in the pursuit of truth and knowledge serving the human society, especially through the creative use of inter-disciplinary approaches.</p>
              <p style="text-align: justify;">Aquinas College of Higher Studies is also registered with the Tertiary and Vocational Educational Commission (TVEC) of Sri Lanka to conduct Professional Courses and Examinations at the Tertiary Level. The College also works in close collaboration with foreign Universities and Higher Educational Authorities.</p>
	</div>
              <p style="text-align: justify;">Aquinas College of Higher Studies is now a Degree Awarding Institute, approved by the University Grants Commission (UGC) on 22<sup>nd</sup> December, 2004, and was gazetted by the then Hon. Minister of Education on 10<sup>th</sup> October 2005.</p>
              <p style="text-align: justify;">Aquinas College of Higher Studies offers a wide range of full – time and part – time degree, diploma and certificate courses. We are proud to say, that we have among our past students, over 640 London Graduates and a number of local University Graduates who have done their external degrees at Aquinas College. Most of the students who have gone through Aquinas have left footprints in prestigious national and international professional centers to acclaim higher standards in a vast spectrum, in almost, all fields.</p>
